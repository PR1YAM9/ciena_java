package fundamentals;

class dataTypes{
    public static void main(String[] args) {
        int num = 10;
        double num2 = 10.5;
        char letter = 'A';
        boolean flag = true;

        System.out.println("Integer: " + num);
        System.out.println("Double: " + num2);
        System.out.println("Character: " + letter);
        System.out.println("Boolean: " + flag);
    }
}